Userinyerface – Worst UI Practice

This is a quiz, right? I was nervous that time because I didn’t review anything about the lesson,
and I thought that I will get low score or even a zero. But miss said that we shouldn’t get nervous and we
shouldn’t review anything because this quiz would be fun, and we would enjoy a lot. When I first read
the word “Userinyerface”, I thought of activities about designing an interface of a program. But then, I
was wrong.

It was a tricky puzzle game and we really had a hard time to continue to the next level. We tried
helping each other just to get out of a level. It was tricky because at first, we couldn’t find the close
button when the page says that it has been locked. I think that this is a lesson that I should remember
when I am designing an interface. I think that buttons should be put in a good position wherein the user
can see where to close a page or to go to the next page. Also, there is a part of the game wherein I can’t
exit the terms and condition and I must scroll down to the bottom of the terms and conditions page just
to exit that page. Maybe, the lesson that the puzzle is trying to imply is that I should have a better
interface since the use of interface is to have a better communication between the application and the
user. It was fun doing the puzzle, but I finished the puzzle at around 7 minutes, I think. I tried it for the
second time and since I already know what to do and what not to do, I finished it for 1 minute and 30
seconds. It was challenging because there are tricky parts and it was also fun since we helped each
other, and no one got stressed out playing the game.

As a conclusion, Userinyerface is a fun puzzle game and a meaningful game. Since the game
shows how bad can an interface be if the features/buttons will be placed in a bad position. The user will
tend to get confused when they use an application that has a bad interface since he or she might not
know what to do and how to access the application. All in all, I had fun playing that game and it boost
my competitive side so that I can finish the puzzle as fast as I could.

      </p>

