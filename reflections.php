    <h1 class="hi">

      Reflection #1

    </h1>

    <pre>

      <p>

In this week of learning, I learned about the design process. The design process is a process
wherein you have a certain problem that needs to be solved. The process includes the problem itself,
creating a solution/s, next would be analyzing the solutions and choosing what is best fitted for the
problem and most efficient way to solve the problem, next is embodying the solutions or simply
implement the solution, last would be assess, check for another incoming problems that needs to be
solved.
I have learned that the design process is a process that can be used on any situation. For
example, I have a problem about my money since I don’t have enough savings to buy materials for my
project. First, in order to solve that, of course I need the problem and the problem would be how to
save money to buy my needs. Next, I would generate some solutions, first solution would be I need to
save about 20 pesos per day and keep it in a safe and once the day has come that I need to buy my
needs, I have savings that I can spend with. But, the “generating solution” part of the process should not
just have one solution since if that solution won’t work, there would be a backup plan or a backup
solution. Another solution is to lessen my expenses. If I am eating too much since, I crave a lot for foods,
I will try to lessen my expenses in foods so I can have extra money to buy for the material that I needed.
Now that I have generated two solutions, I can now go to the next step of the process which is the
analyzing and choosing a solution that best fits the problem. Now, I think that the best solution would
be saving 20 pesos per day because this will be surely kept in a safe and it will be secured and used only
for future important purposes. Next step would be embodying the solution, I will now implement the
solution that I created and start saving 20 pesos per day. Next, assess, I would look for another problem
like if the 20 pesos is not enough to buy my needs, I would gather again some solutions that can help me
solve another problem.
The design process is quite helpful since it teaches us to solve a problem in an organize manner.
I learned that problems especially in programming and other school-related stuff can’t be easily solved
without a process. Design process would be a great help in addition to my knowledge because I can now
solve problems in school using a process. For me, using this process would help me lessen the thoughts
that comes up in my mind because there is a procedure that needs to be followed and my mind won’t
jump from a step to another step. Thoughts would not intersect with each other because there is a
process to follow.

      </p>

    </pre>

